package itTalkDO;

public class Ad {

	private int ad_no;
	private String ad_id;
	private String ad_pw;
	private String ad_email;
	private String ad_nick;
	
	public int getAd_no() {
		return ad_no;
	}
	public void setAd_no(int ad_no) {
		this.ad_no = ad_no;
	}
	public String getAd_id() {
		return ad_id;
	}
	public void setAd_id(String ad_id) {
		this.ad_id = ad_id;
	}
	public String getAd_pw() {
		return ad_pw;
	}
	public void setAd_pw(String ad_pw) {
		this.ad_pw = ad_pw;
	}
	public String getAd_email() {
		return ad_email;
	}
	public void setAd_email(String ad_email) {
		this.ad_email = ad_email;
	}
	public String getAd_nick() {
		return ad_nick;
	}
	public void setAd_nick(String ad_nick) {
		this.ad_nick = ad_nick;
	}
	
	
}
